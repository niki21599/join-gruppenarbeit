async function backlog() {
    await loadTasksFromServer();

    for (let i = 0; i < tasks.length; i++) {
        let task = tasks[i]
        if (task.section === "backlog") {
            let avatarImg = task.user.img
            let name = task.user.name
            let email = task.user.email
            let category = task.category;
            let description = task.description
            let color = task.user.color;


            document.getElementById('backlog').innerHTML += `

            <div class="task" style="border-left: 10px solid ${color};">

                <div class="assign">
                    <img class="backlog-img" src="${avatarImg}">
                    <div class="data">
                        <span>${name}</span>
                        <span style="color:rgb(157, 158, 247)">${email}</span>
                    </div>
                </div>
                <div class="category">
                    <span>${category}</span>
                </div>
                <div class="details">
                    <span>${description}</span>
                </div>
        
            </div>`;
        }
    }
}