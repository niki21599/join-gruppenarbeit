class Task {
    id;
    title;
    section;
    description;
    date;
    urgency;
    user;
    category;
    color

    constructor(id, title, section, description, date, urgency, user, category, color) {
        this.id = id;
        this.title = title;
        this.section = section;
        this.description = description;
        this.date = date;
        this.urgency = urgency;
        this.user = user;
        this.category = category;
        this.color = color;
    }


}